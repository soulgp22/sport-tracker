import fs from 'node:fs/promises';
import path from 'node:path';

const ROOT = process.cwd();
const catalogPath = path.join(ROOT, 'src/data/exercises.catalog.json');
const coreCatalogPath = path.join(ROOT, 'src/data/exercises.core.json');
const remoteCatalogPath = path.join(ROOT, 'community/exercises-all.json');
const manifestPath = path.join(ROOT, 'community/index.json');
const starterFiles = ['full-body-3.json', 'ppl-3.json', 'upper-lower-4.json'];

const catalog = JSON.parse(await fs.readFile(catalogPath, 'utf8'));
const byName = new Map();
for (const exercise of catalog) {
  byName.set(exercise.name.trim().toLowerCase(), exercise);
  if (exercise.nameFr) byName.set(exercise.nameFr.trim().toLowerCase(), exercise);
}

const coreIds = new Set();
for (const filename of starterFiles) {
  const payload = JSON.parse(await fs.readFile(path.join(ROOT, 'community', filename), 'utf8'));
  for (const program of payload.programs ?? []) {
    for (const day of program.days ?? []) {
      for (const item of day.exercises ?? []) {
        const names = [item.exerciseName, ...(item.alternativeExerciseNames ?? [])].filter(Boolean);
        for (const name of names) {
          const exercise = byName.get(name.trim().toLowerCase());
          if (!exercise) throw new Error(`Exercice introuvable dans le catalogue : ${name}`);
          coreIds.add(exercise.id);
        }
      }
    }
  }
}

const coreCatalog = catalog.filter((exercise) => coreIds.has(exercise.id));
const remoteCatalog = catalog.filter((exercise) => !coreIds.has(exercise.id));
await fs.writeFile(coreCatalogPath, `${JSON.stringify(coreCatalog, null, 2)}\n`);
await fs.mkdir(path.dirname(remoteCatalogPath), { recursive: true });
await fs.writeFile(remoteCatalogPath, `${JSON.stringify({ version: 1, exercises: remoteCatalog }, null, 2)}\n`);

const registryLines = coreCatalog.map(
  (exercise) =>
    `  '${exercise.id}': require('../../assets/exercises/core/${exercise.id}.webp'),`
);
await fs.writeFile(
  path.join(ROOT, 'src/data/exercises.gifs.ts'),
  `/* Generated by scripts/build-content-packs.mjs. */\n` +
    `import type { ImageSource } from 'expo-image';\n\n` +
    `export const exerciseGifs: Record<string, ImageSource> = {\n${registryLines.join('\n')}\n};\n`
);

const manifest = JSON.parse(await fs.readFile(manifestPath, 'utf8'));
manifest.version = 3;
manifest.exercisePacks = [
  {
    id: 'exercises-complete-v1',
    name: 'Catalogue complet d’exercices',
    description: `${remoteCatalog.length} exercices supplémentaires avec instructions et animations.`,
    author: 'Life Sport Tracker',
    level: 'Tous niveaux',
    exercisesCount: remoteCatalog.length,
    file: 'exercises-all.json',
    mediaBaseUrl: 'https://raw.githubusercontent.com/soulgp22/sport-tracker/main/assets/exercises/gifs/'
  }
];
await fs.writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);

console.log(`Catalogue intégré : ${coreCatalog.length} exercices`);
console.log(`Pack GitHub : ${remoteCatalog.length} exercices`);
console.log(`IDs intégrés : ${[...coreIds].join(', ')}`);
